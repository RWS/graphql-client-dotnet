
package com.sdl.web.pca.client.contentmodel.pagemodeldata;

public class Meta {

    private String sitemapKeyword;
    private String twitterCard;
    private String ogTitle;
    private String ogType;
    private String ogLocale;
    private String ogDescription;
    private String description;

    public String getSitemapKeyword() {
        return sitemapKeyword;
    }

    public void setSitemapKeyword(String sitemapKeyword) {
        this.sitemapKeyword = sitemapKeyword;
    }

    public String getTwitterCard() {
        return twitterCard;
    }

    public void setTwitterCard(String twitterCard) {
        this.twitterCard = twitterCard;
    }

    public String getOgTitle() {
        return ogTitle;
    }

    public void setOgTitle(String ogTitle) {
        this.ogTitle = ogTitle;
    }

    public String getOgType() {
        return ogType;
    }

    public void setOgType(String ogType) {
        this.ogType = ogType;
    }

    public String getOgLocale() {
        return ogLocale;
    }

    public void setOgLocale(String ogLocale) {
        this.ogLocale = ogLocale;
    }

    public String getOgDescription() {
        return ogDescription;
    }

    public void setOgDescription(String ogDescription) {
        this.ogDescription = ogDescription;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}